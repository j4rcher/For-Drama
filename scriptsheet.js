function openPunchdrunk() {
  document.getElementById("punchdrunk").style.animation = "fade-in 1s forwards";
}

function openGecko() {
  document.getElementById("gecko").style.animation = "fade-in 1s forwards";
}

function openPaperBirds() {
  document.getElementById("paper-birds").style.animation = "fade-in 1s forwards";
}

function closeBtn() {
  document.getElementById("punchdrunk").style.animation = "fade-out 1s forwards";
  document.getElementById("gecko").style.animation = "fade-out 1s forwards";
  document.getElementById("paper-birds").style.animation = "fade-out 1s forwards";
}
